#!/usr/bin/env python
import re

with open('seed-inventory.sql', 'r') as f:
    content = f.read()

# Map of MM-DD to DD-MM conversions
conversions = {
    "'01-15'": "'15-01'",
    "'02-01'": "'01-02'",
    "'02-15'": "'15-02'",
    "'02-28'": "'28-02'",
    "'03-01'": "'01-03'",
    "'03-15'": "'15-03'",
    "'03-31'": "'31-03'",
    "'04-01'": "'01-04'",
    "'04-15'": "'15-04'",
    "'04-30'": "'30-04'",
    "'05-01'": "'01-05'",
    "'05-15'": "'15-05'",
    "'05-31'": "'31-05'",
    "'06-01'": "'01-06'",
    "'06-15'": "'15-06'",
    "'06-20'": "'20-06'",
    "'06-30'": "'30-06'",
    "'07-01'": "'01-07'",
    "'07-31'": "'31-07'",
    "'08-01'": "'01-08'",
    "'08-31'": "'31-08'",
    "'09-01'": "'01-09'",
    "'09-30'": "'30-09'",
    "'10-01'": "'01-10'",
    "'10-15'": "'15-10'",
    "'10-31'": "'31-10'",
    "'11-30'": "'30-11'",
    "'12-15'": "'15-12'",
    "'12-31'": "'31-12'"
}

for mmdd, ddmm in conversions.items():
    content = content.replace(mmdd, ddmm)

with open('seed-inventory.sql', 'w') as f:
    f.write(content)

print('All dates converted from MM-DD to DD-MM format')
